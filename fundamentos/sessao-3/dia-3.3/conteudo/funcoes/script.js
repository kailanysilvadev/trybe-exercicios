let sale = 200;

function addSale(value){
  return sale + value;
};

addSale(20);


function subtractSale(value){
  return sale - value;
};

subtractSale(20);

function multiplySale(value){
  return sale * value;
};

multiplySale(2);

function divideSale(value){
  return sale / value;
};

divideSale(2);
