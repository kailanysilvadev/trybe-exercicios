let sale = 200;
let tx = 0;

function addSale(value){
  return sale + value;
};

addSale(20);


function subtractSale(value){
  return sale - value;
};

subtractSale(20);

function multiplySale(value){
  tx = sale * value
  return sale += tx;
};

multiplySale(0.10);

function divideSale(value){
  return sale / value;
};

divideSale(2);
